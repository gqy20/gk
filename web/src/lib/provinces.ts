/**
 * 获取某省某所学校的坐标。
 * 北京特殊处理：均匀分散在多个点位上。
 */
export function getSchoolCoord(
  province: string,
  schoolIndex: number,
  totalInProvince: number,
): [number, number] {
  if (province === "北京") {
    const idx = schoolIndex % BEIJING_SPREAD_COORDS.length;
    return BEIJING_SPREAD_COORDS[idx];
  }

  const base = PROVINCE_COORDS[province] || [104.065735, 30.659462];
  if (totalInProvince <= 1) return base;

  const angle = (Math.PI * 2 * schoolIndex) / totalInProvince;
  const radius = Math.min(1.15, 0.22 + totalInProvince * 0.035);
  return [
    Number((base[0] + Math.cos(angle) * radius).toFixed(6)),
    Number((base[1] + Math.sin(angle) * radius * 0.72).toFixed(6)),
  ];
}
